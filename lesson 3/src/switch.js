const season = 'dang!';

switch (season) {
    case 'winter': {
        console.log('It is cold outside. Let\'s drink some tea!');
        break;
    }
    case 'spring': {
        console.log('Birds are singing...');
        break;
    }
    case 'summer': {
        console.log('Sunbathing and swimming season!');
        break;
    }
    case 'autumn': {
        console.log('Back to school...');
        break;
    }
    default: {
        console.log('Read about the seasons and come back later!');
    }
}